/**
* @brief This function handles USART2 global interrupt.
*/
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
	uint32_t isrflags   = READ_REG((&huart2)->Instance->SR);
  uint32_t cr1its     = READ_REG((&huart2)->Instance->CR1);
  uint32_t cr3its     = READ_REG((&huart2)->Instance->CR3);
  
	usart2_rx[0]=(uint8_t)((&huart2)->Instance->DR & (uint8_t)0x00FF);
	
	if((ext_device & temp_sensor) && (temp_type & SHT20) && (rs485_select==2)){
			sht_rec_buffer[temp_rec_len]=usart2_rx[0];
			temp_rec_len++;
			if(temp_rec_len>13)temp_rec_len=0;
		}
		if((ext_device & camera) && (rs485_select==1)){
			
			CameraBufPointer[CamRecLen]=usart2_rx[0];
			CamRecLen++;
			if(CamRecLen>=512){
				CamRecLen=0;
				if(PictureRecProcess==1){
					if(CamBufOvf==0){
						CameraBufPointer=CameraBuffer1;
						CamBufOvf=1;
						
					}
					else{
						CameraBufPointer=CameraBuffer;
						CamBufOvf=0;
					}
									
				}
			}

			if(PictureRecProcess==1){
				
				if(PictureSizeCounter != 0){ PictureSizeCounter++;}
				if(CamRecLen==5 && PictureSizeCounter==0) {PictureSizeCounter++;CamRecLen=0;}
				if(PictureSizeCounter==PictureSize){
					PictureRecProcess=0;
							 
				}
				
			}
					
		}
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */

  /* USER CODE END USART2_IRQn 1 */
}

